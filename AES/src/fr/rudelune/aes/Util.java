	static String getByteArrayAsNumber(byte[] data) {
		StringBuilder buffer = new StringBuilder();
		for (byte b : data) {
			buffer.append(getStrRepresentationOfHex(b));
		}
		return buffer.toString();
	}

	/**
	 * Renvoie un octet sous forme hexadécimale.
	 *
	 * @param nb
	 *            Octet dont la représentation sous forme hexadécimale est à déterminer.
	 * @return Chaîne de caractère représentant un octet sous forme hexadécimale.
	 */
	private static String getStrRepresentationOfHex(byte nb) {
		String result = Integer.toHexString(nb & 0xFF);
		return result.length() == 2 ? result : '0' + result;
	}

	/**
	 * Met un tableau de 4n octets sous la forme d'une matrice 4*n d'octets.
	 *
	 * @param data
	 *            Tableau de 4n octets.
	 * @return Matrice de 4*n octets.
	 */
	static byte[][] convertByteArrayToByteMatrix(byte[] data) {
		byte length = (byte) (data.length / 4);
		byte[][] result = new byte[4][length];
		for (byte i = 0; i < length; i++) {
			for (byte j = 0; j < 4; j++) {
				result[j][i] = data[4 * i + j];
			}
		}
		return result;
	}

	/**
	 * Met une matrice d'octets sous forme d'un tableau d'octets
	 *
	 * @param data
	 *            Matrice d'octets
	 * @return Tableau d'octets
	 */
	public static byte[] convertByteMatrixToByteArray(byte[][] data) {
		byte[] result = new byte[data.length * data[0].length];
		for (int j = 0, total = 0; j < data[0].length; j++) {
			for (int i = 0; i < data.length; i++, total++) {
				result[total] = data[i][j];
			}
		}
		return result;
	}

	/**
	 * Organise une chaîne de caractère contenant 4*n octets sous forme hexadécimales dans une matrice 4*n d'octets.
	 *
	 * @param string
	 *            Chaîne de caractère contenant 4*n octets sous forme hexadécimales.
	 * @return Matrice 4*n d'octets.
	 */
	static byte[][] convertStrToByteMatrix(String string) {
		Pattern pattern = Pattern.compile(".{2}");
		Matcher matcher = pattern.matcher(string);
		int length = string.length() / 8;
		byte[][] result = new byte[4][length];
		for (int i = 0; i < length; i++) {
			for (byte j = 0; j < 4; j++) {
				if (matcher.find()) {
					result[j][i] = (byte) Integer.parseInt(matcher.group(), 16);
				}
			}
		}
		return result;
	}

	/**
	 * Transforme une matrice d'integers en matrice d'octets. Utilisé pour améliorer la lisibilité de la création des
	 * SBOX.
	 *
	 * @param matrix
	 *            matrice d'integers à transformer.
	 * @return matrice d'octets obtenue à l'aide la matrice fournie.
	 */
	static byte[][] convertIntMatrixToByteMatrix(int[][] matrix) {
		byte[][] result = new byte[matrix.length][matrix[0].length];
		for (int i = 0; i < matrix.length; i++) {
			for (int j = 0; j < matrix[0].length; j++) {
				result[i][j] = (byte) matrix[i][j];
			}
		}
		return result;
	}

	/**
	 * Effectue l'opération OU-exclusif sur l'ensemble des composantes d'une matrice 4*4.
	 *
	 * @param matrix1
	 *            Première matrice 4*4.
	 * @param matrix2
	 *            Deuxième matrice 4*4.
	 * @return Matrice 4*4 contenant le résultat de l'opération OU-exclusif sur l'ensemble des composantes des 2
	 *         matrices.
	 */
	static byte[][] xor(byte[][] matrix1, byte[][] matrix2) {
		byte[][] result = new byte[4][];
		for (byte i = 0; i < 4; i++)
			result[i] = xor(matrix1[i], matrix2[i]);
		return result;
	}

	/**
	 * Effectue l'opération OU-exclusif sur l'ensemble des composantes de 2 tableaux de taille n.
	 *
	 * @param array1
	 *            Premier tableau de taille n.
	 * @param array2
	 *            Deuxième tableau de taille n.
	 * @return Tableau de taille n contenant le résultat de l'opération OU-exclusif sur l'ensemble des composantes des 2
	 *         tableaux.
	 */
	static byte[] xor(byte[] array1, byte[] array2) {
		assert array1.length == array2.length;
		byte[] result = new byte[array1.length];
		for (byte i = 0; i < array1.length; i++)
			result[i] = (byte) (array1[i] ^ array2[i]);
		return result;
	}

	/**
	 * Décale les lignes 2, 3 et 4 d'une matrice 4*4 respectivement de 1, 2 et 3 cases vers la droite si inverse est
	 * vrai et vers la gauche si inverse est faux.
	 *
	 * @param matrix
	 *            Matrice 4*4.
	 * @param inverse
	 *            Décalage vers la droite ou la gauche.
	 * @param minsc
	 *            Utilisation de l'algorithme MINSC.
	 * @return Matrice 4*4 résultat.
	 */
	static byte[][] shift(byte[][] matrix, boolean inverse, boolean minsc) {
		byte[][] result = new byte[4][4];
		for (byte i = 0; i < 4; i++)
			if (minsc) {
				result[i] = shift(matrix[i], (byte) (2 * (i % 2)));
			} else {
				result[i] = shift(matrix[i], (inverse ? i : (byte) -i));
			}
		return result;
	}

	/**
	 * Effectue une rotation des composantes d'un tableau d'octets de how cases vers la droite.
	 *
	 * @param array
	 *            Tableau d'octets.
	 * @param how
	 *            Nombre de rotations vers la droite à effectuer.
	 * @return Tableau d'octets.
	 */
	static byte[] shift(byte[] array, byte how) {
		if (how % array.length == 0)
			return array;
		if (how < -array.length)
			how %= array.length;
		byte[] result = new byte[array.length];
		for (byte i = 0; i < array.length; i++)
			result[ (i + array.length + how) % array.length] = array[i];
		return result;
	}

	/**
	 * Remplace les composantes d'une matrice d'octets par les octets qui leurs correspondent dans la table de
	 * substitution ou son inverse.
	 *
	 * @param matrix
	 *            Matrice d'octets.
	 * @param inverse
	 *            Utilisation de la table de substitution ou de son inverse.
	 * @param minsc
	 *            Utilisation de l'algorithme MINSC.
	 */
	static void replaceSBOX(byte[][] matrix, boolean inverse, boolean minsc) {
		for (byte i = 0; i < matrix.length; i++) {
			matrix[i] = replaceSBOX(matrix[i], inverse, minsc);
		}
	}

	/**
	 * Remplace les composantes d'un tableau d'octets par les octets qui leurs correspondent dans la table de
	 * substitution ou son inverse.
	 *
	 * @param array
	 *            Tableau d'octets.
	 * @param inverse
	 *            Utilisation de la table de substitution ou de son inverse.
	 * @param minsc
	 *            Utilisation de l'algorithme MINSC.
	 * @return Tableau d'octets.
	 */
	static byte[] replaceSBOX(byte[] array, boolean inverse, boolean minsc) {
		for (byte i = 0; i < array.length; i++)
			array[i] = getSBOX(array[i], inverse, minsc);
		return array;
	}

	/**
	 * Renvoie l'octet associé à un autre dans la table de substitution ou son inverse.
	 *
	 * @param nb
	 *            Octet.
	 * @param inverse
	 *            Utilisation de la table de substitution ou de son inverse.
	 * @param minsc
	 *            Utilisation de l'algorithme MINSC.
	 * @return Octet correspondant.
	 */
	private static byte getSBOX(byte nb, boolean inverse, boolean minsc) {
		int nbu = nb & 0xFF;
		int x = nbu >> 4;
		int y = nbu & 0xF;
		if (minsc) {
			return SBOX_MINSC[x][y];
		} else {
			if (inverse) {
				return SBOXINV_AES[x][y];
			} else {
				return SBOX_AES[x][y];
			}
		}
	}

