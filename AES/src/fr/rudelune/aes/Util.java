	/**
	 * Génère une matrice de taille 4*width d'octets aléatoirement.
	 *
	 * @param width
	 *            Nombre de colonnes de la matrice.
	 * @return La matrice générée.
	 */
	static byte[][] generateRandomByteMatrix(int width) {
		Random ran = new Random();
		byte[][] result = new byte[4][width];
		for (byte i = 0; i < 4; i++)
			for (int j = 0; j < width; j++)
				result[i][j] = (byte) ran.nextInt(0x100);
		return result;
	}

